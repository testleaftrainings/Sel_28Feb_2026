package org.testleaf.week1.day2;

public class LearnForLoop {

   public static void main(String[] args) {
       //To print numbers from 1 to 5
       // 1   2   3   4   5
      
       for(int i=1;i<5;i++){
        System.out.println(i);
    }

       System.out.println("======================");
       
       //To print numbers from 1 to 5
       // 1   2   3   4   5
       //Reverse for loop
       for(int i=5;i>=1;i--){
       System.out.println(i);
        }
     }
}




